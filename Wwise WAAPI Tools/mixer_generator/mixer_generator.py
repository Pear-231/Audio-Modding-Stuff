def createMixer(mixerName):
    return  {
        "parent": "\\Actor-Mixer Hierarchy\\Default Work Unit",
        "type": "ActorMixer",
        "name": mixerName
    }    

try:
    # Connecting to Waapi using default URL
    with WaapiClient() as client:

        for mixer in mixers:
            # Create the Dialogue Event
            client.call("ak.wwise.core.object.create", createMixer(mixer))
        
            # Get info about created Dialogue Event
            mixerArgs = {
                "from": {
                    "path": ["\\Actor-Mixer Hierarchy\\Default Work Unit\\" + mixer]
                },
                "options": {
                    "return": ["id", "name"]
                }
            }
            createdMixer = client.call("ak.wwise.core.object.get", mixerArgs)
            print("Created DialogueEvent: " + createdMixer['return'][0]['name'])

except CannotConnectToWaapiException:
    print("Could not connect to Waapi: Is Wwise running and Wwise Authoring API enabled?")

except Exception as e:
    print(str(e))